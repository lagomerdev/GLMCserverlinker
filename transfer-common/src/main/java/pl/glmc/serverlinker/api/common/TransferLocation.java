package pl.glmc.serverlinker.api.common;

public class TransferLocation {

    private final double x, y, z;

    public TransferLocation(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getZ() {
        return z;
    }
}
