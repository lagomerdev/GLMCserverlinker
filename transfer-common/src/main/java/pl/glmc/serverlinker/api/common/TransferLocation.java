package pl.glmc.serverlinker.api.common;

public class TransferLocation {

    private final int x, y, z;

    public TransferLocation(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }
}
